// This program demonstrate boolean variables.
    // Boolean variables are set to either true(1) or false(0)


#include <iostream>
using namespace std;

int main()
{
    bool boolValue;

    boolValue = true;
    cout << boolValue << endl; // output will be 1
    boolValue = false;
    cout << boolValue << endl; // output will be 0
    return 0;
}